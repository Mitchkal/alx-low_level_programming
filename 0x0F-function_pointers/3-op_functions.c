#include <stdio.h>
/**
 * op_add - adds integers
 * @a : integer 1
 * @b : integer 2
 * Return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts integers
 * @a : integer 1
 * @b : integer 2
 * Return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies integers
 * @a : integer 1
 * @b : integer 2
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides integers
 * @a : integer 1
 * @b : integer 2
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gives the rmainderof division
 * @a : integer 1
 * @b : integer 2
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}


